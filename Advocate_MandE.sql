create database advocatem_and_e;

use database advocatem_and_e;

create table CLIENT_APPLICATION_FORM(
ID INT NOT NULL auto_increment,
FIRST_NAME VARCHAR(50) NOT NULL,
LAST_NAME VARCHAR(50) NOT NULL,
MIDDLE_NAME VARCHAR(50) NOT NULL,
SEX VARCHAR(6) NULL,
DATE_OF_BIRTH DATETIME NOT NULL,
CONTACT VARCHAR(50) NOT NULL,
COUNTRY_OF_BIRTH VARCHAR(50) NOT NULL,
RESIDENCE_STATUS VARCHAR(20) NOT NULL,
LC_ZONE VARCHAR(50) NOT NULL,
STREET VARCHAR(50) NOT NULL,
DISTRICT VARCHAR(50) NOT NULL,
COUNTY VARCHAR(50) NOT NULL,
MARITAL_STATUS VARCHAR(50) NOT NULL,
DATE DATETIME NOT NULL,
PRIMARY KEY (ID)
);

create table SCANNED_DOCS(
ID INT NOT NULL auto_increment,
SCANNED_DOCS_ID INT references advocatem_and_e(ID),
CLIENT_NAME VARCHAR(50) NOT NULL,
CLIENT_DOCS BLOB NULL,
PRIMARY KEY (ID)
);


create table LEGAL_AID_ACCOUNTABILITY_FORM(
ID INT NOT NULL auto_increment,
LAWYER_NAME VARCHAR(50) NOT NULL,
CLIENT_NAME_OR_NUMBER VARCHAR(50) NOT NULL,
DESTINATION VARCHAR(50) NOT NULL,
PURPOSE VARCHAR(50) NOT NULL,
ANTICIPATED_ VARCHAR(50) NOT NULL,
AMOUNT DOUBLE NOT NULL,
APPROVED_BY_PRESIDENT VARCHAR(4) NOT NULL,
APPROVED_BY_ACCOUNTS VARCHAR(4) NOT NULL,
DATE DATETIME NOT NULL,
PRIMARY KEY (ID)
);

create table DISABILITY(
ID INT NOT NULL auto_increment,
DISABILITY_ID INT references LEGAL_AID_ACCOUNTABILITY_FORM(ID),
NAME_OF_DISABILITY VARCHAR(30) NOT NULL,
DESCRIPTION VARCHAR(30) NOT NULL,
PRIMARY KEY (ID)
);





create table ASSESMENT(
ID INT NOT NULL auto_increment,
IS_EMPLOYED VARCHAR(5) NOT NULL,
LAST_PAID_WORK VARCHAR(40) NOT NULL,
KIND_OF_WORK VARCHAR(40) NOT NULL,
SELF_EMPLOYED  VARCHAR(5) NOT NULL,
DIRECTOR_IN_A_COMPANY VARCHAR(40) NOT NULL,
BUSINESS VARCHAR(40) NOT NULL,
ANYOTHER_INCOME VARCHAR(30) NOT NULL,
DETAILS_OF_ASSETS VARCHAR(70) NOT NULL,
RENT VARCHAR(30) NOT NULL,
OTHERS VARCHAR(40) NOT NULL,
DESCRIPTION VARCHAR(30) NOT NULL,
NUMBER_HOUSE_COST VARCHAR(30) NOT NULL,
ANYOTHER_PAYMENT VARCHAR(30) NOT NULL,
PRIMARY KEY (ID)
);

create table SIDE_INCOME(
ID INT NOT NULL auto_increment,
SIDE_INCOME_ID INT references ASSESMENT(ID),
AMOUNT DOUBLE NOT NULL,
MONTHLY_INCOME DOUBLE  NULL,
ANNUAL_INCOME DOUBLE  NULL,
PRIMARY KEY (ID)
);




